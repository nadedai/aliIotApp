package com.example.t3.models;

public class SwitchBean {
    private int counter;
    private String counterTime;
    private int powerSwitch;

    public int getCounter() {
        return counter;
    }

    public void setCounter(int counter) {
        this.counter = counter;
    }

    public String getCounterTime() {
        return counterTime;
    }

    public void setCounterTime(String counterTime) {
        this.counterTime = counterTime;
    }

    public int getPowerSwitch() {
        return powerSwitch;
    }

    public void setPowerSwitch(int powerSwitch) {
        this.powerSwitch = powerSwitch;
    }

    public String getPowerSwitchTime() {
        return powerSwitchTime;
    }

    public void setPowerSwitchTime(String powerSwitchTime) {
        this.powerSwitchTime = powerSwitchTime;
    }

    private String powerSwitchTime;



}
